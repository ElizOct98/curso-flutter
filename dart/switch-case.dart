void main() {
  String color = 'gris';

  switch (color) {
    case 'verde':
      print("Verde como la bandera");
      break;
    case 'rojo':
      print("Rojo como la sangre");
      break;
    case 'azul':
      print("Azul como el principe");
      break;
    default:
      print('No hay ningun color registrado');
  }
}
